char	*dou_arg(va_list ap, char c, t_options *opt)
{
	char	*str;

	if (c == 'D')
		c = 'd';
	if (c == 'O')
		c = 'o';
	if (c == 'U')
		c = 'u';
	opt->taille = 3;
	return (int_arg(ap, c, opt));
}
