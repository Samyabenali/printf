int		ft_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb >= 1)
	{
		while (i * i < nb)
		{
			i++;
		}
		if (i * i == nb)
			return (i);
		else
			return (0);
	}
	else
		return (0);
}
