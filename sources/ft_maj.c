#include "printf.h"

char	*ft_maj(char *res)
{
	int		i;

	i = 0;
	while (res[i])
	{
		if (res[i] >= 97 && res[i] <= 122)
			res[i] = res[i] - 32;
		i++;
	}
	return (res);
}
