#include "printf.h"

intmax_t	ft_atoi_max(char *s)
{
	int			i;
	intmax_t	a;
	intmax_t	b;

	i = 0;
	a = 1;
	b = 0;
	while (s[i] == '\n' || s[i] == '\b' || s[i] == '\t' ||
			s[i] == '\v' || s[i] == '\f' || s[i] == '\r' || s[i] == ' ')
		i++;
	if (s[i] == '-')
		a = -1;
	if (s[i] == '+' || s[i] == '-')
		i++;
	while (s[i] >= 48 && s[i] <= 57)
	{
		b = b * 10 + s[i] - 48;
		i++;
	}
	return (a * b);
}
